package cat.breadcat.toolbox.units;

public final class DataUnits
{
    private DataUnits() {}


    public static final int BYTE = 1;

    public static final int KB = 1000;
    public static final int MB = KB * 1000;
    public static final int GB = MB * 1000;

    public static final int KiB = 1024;
    public static final int MiB = KiB * 1024;
    public static final int GiB = MiB * 1024;
}
